#define CLK     LATBbits.LATB7  // CLK
#define DATA    LATBbits.LATB8  // DATA   
#define CS      LATBbits.LATB9  // CS

void initPins( void );
void lcdTest( void );
void initMAX7219( void );
void SPIout( unsigned char );
void pulseCS( void );
void writeMAX7219( char address, char data);

void initPins( void )
{
    TRISB = 0;
    TRISA = 0;
    AD1PCFG = 0xFFFF;
}

void lcdTest( void )
{
    lcdPrint("helloworld");
    setCursor(0xC0);
    lcdIntPrint(10);
    lcdPrint(" ");
    lcdFloatPrint(3.25);
    lcdPrint(" ");
    lcdIntPrint(sizeof("helloworld"));
}

void initMAX7219( void )
{
    CS = LOW;
    
    SPIout( 0x09 );
    SPIout( 0x00 );
    pulseCS();
    
    SPIout( 0x0A );
    SPIout( 0x03 );
    pulseCS();

    SPIout( 0x0B );
    SPIout( 0x07 );
    pulseCS();

    SPIout( 0x0C );
    SPIout( 0x01 );
    pulseCS();

    SPIout( 0x0F );
    SPIout( 0x00 );
    pulseCS();
}

void writeMAX7219( char address, char data)
{
    SPIout( address );
    SPIout( data );
    pulseCS();
}

void SPIout( unsigned char val )
{
    int j;
    unsigned char temp;
    for ( j = 0; j < 8;  j++ )  // Shift out MSB first
    {
        temp = val & 0x80;    // MSB out first
        if ( temp == 0x80 )
            DATA = HIGH;
        else
            DATA = LOW;
        
        CLK = HIGH;
        delay_us(20);
        CLK = LOW;
        
        val = val << 1;
    }
}

void pulseCS( void )
{
    CS = HIGH;
    delay_ms( 1 );
    CS = LOW;
}

int main(void) 
{
    initPins();
    lcdInit();
    lcdTest();
    initMAX7219();
    
    int i, j;
    for( j = 0; j < 38; j++ )
    {
        for( i = 0; i < 9; i++ )
        {
            writeMAX7219( i, disp1[ j ][i - 1] );   
            delay_ms(1000);
        }
        
        delay_ms( 500 );
        
        if (j == 37)
            j = -1;
    }
    while(1){
        LATBbits.LATB5 = LOW;
        delay_ms(1000);
        LATBbits.LATB5 = HIGH;
        delay_ms(1000);
    }
        
    return 0;
}